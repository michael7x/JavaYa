package capitulo10;

public class EstructuraRepetitivaWhile1 {
	
	public static void main(String[] ar){
		int x = 1;
		
		while (x <= 100){
			System.out.print(x);
			System.out.print(" - ");
			x = x + 1;
		}
	}

}
